import numpy as np

arr_2d=np.array([[1, 2, 3],
                [4, 5, 6]])

print("Array Shape : ")
print(arr_2d.shape)     # (2,3) -- > (row, column)

print("Array Size : ")
print(arr_2d.size)      # total number of elements

print("Array Dimension : ")
print(arr_2d.ndim)      # 1D, 2D, 3D

print("Array DataType : ")
print(arr_2d.dtype)      # Number/String


